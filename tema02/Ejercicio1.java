/**
 * Tema 2
 * Ejercicio 1, crear dos variables y sumar, restar, dividir y multiplicar
 * 
 * @author Natalia Castillo Muñoz
 */
public class Ejercicio1 {
  public static void main(String[] args) {
    int x = 144;
    int y = 999 ;
    
    System.out.println("La variable x = " + x);
    System.out.println("La variable y = " + y);
    System.out.println("La suma es = " + (x + y));
    System.out.println("La resta es = " + (x - y));
    System.out.println("La división es = " + (y / x));
    System.out.println("La multiplicación es = " + (x * y));
    
  }
}
